package Object_class;

public class hash extends Object {
		public static void main(String[] args)
		{
			hash h=new hash();
			System.out.println(h.hashCode());
			
			hash h1=new hash();
			System.out.println(h1.hashCode());
			
			hash h2=new hash();
			System.out.println(h2.hashCode());
		}
	}


